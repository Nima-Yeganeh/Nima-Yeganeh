import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int a = scan.nextInt();
        int n = scan.nextInt();
        int x = scan.nextInt();

        System.out.println(a);
        System.out.println(n);
        System.out.println(x);
    }
}